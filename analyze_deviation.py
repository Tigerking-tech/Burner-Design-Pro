def analyze_a5():
    """分析 NASA 系数中 a[5] 的含义"""
    print("=" * 80)
    print("NASA 系数中 a[5] 的含义分析")
    print("=" * 80)
    print()
    
    print(f"{'物种':<10} {'a[5] (low)':<15} {'-a[5]*R':<15} {'标准 Hf':<15} {'差值':<15}")
    print("-" * 70)
    
    for sp in ['CO2', 'H2O', 'N2', 'O2', 'CH4', 'C3H8']:
        a5 = project_coeffs[sp]['low'][5]
        H_from_a5 = -a5 * R  # kJ/mol
        Hf_std = std_Hf[sp]
        diff = H_from_a5 - Hf_std
        
        print(f"{sp:<10} {a5:<15.2f} {H_from_a5:<15.2f} {Hf_std:<15.2f} {diff:<15.2f}")
    
    print()
    print("结论：a[5] 不是形成焓，而是 H(298)/R，包含了相对于某种参考态的焓值。")
    print()
    
    # 解释偏差来源
    print("=" * 80)
