function getCoeffs(species, T) {
  const data = nasaCoeffs[species]
  if (!data) return null
  return T < data.Tmid ? data.low : data.high
}

function enthalpy(species, T) {
  const a = getCoeffs(species, T)
  if (!a) return 0
  const H_RT = a[0] + a[1] * T / 2 + a[2] * T * T / 3 + a[3] * T * T * T / 4 + a[4] * T * T * T * T / 5 + a[5] / T
  return R * T * H_RT
}

// 验证: 300K 时各物种的 Cp
function cp(species, T) {
  const a = getCoeffs(species, T)
  if (!a) return 0
  return R * (a[0] + a[1] * T + a[2] * T * T + a[3] * T * T * T + a[4] * T * T * T * T)
}

